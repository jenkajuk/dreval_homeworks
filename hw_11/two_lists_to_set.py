import random

print("Програма для визначення кількості різних чисел у списках")

# створюю списки чисел
l_o_n_1 = [random.randrange(0, 7) for _ in range(10)]
l_o_n_2 = [random.randrange(3, 10) for _ in range(10)]
print("Ваш 1-й список чисел:", l_o_n_1,
      "\nВаш 2-й список чисел:", l_o_n_2)

# виводжу кількість різних елементів для 2-х списків одночасно
print("Кількість різних елементів у 2х списках одночасно:",
      len(set(l_o_n_1) | set(l_o_n_2)))
